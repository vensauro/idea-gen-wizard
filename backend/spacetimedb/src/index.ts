export const createRoom = spacetimedb.reducer(
  { roomId: t.string(), teamName: t.string(), userName: t.string() },
  (ctx, { roomId, teamName, userName }) => {
    // roomId should be 6 characters (generated by frontend)
    
    const existing = ctx.db.room.id.find(roomId);
    if (existing) {
        throw new Error("Room already exists.");
    }

    ctx.db.room.insert({
      id: roomId,
      team_name: teamName,
      host_identity: ctx.sender,
      form_data: "{}",
    });
    
    // Check if user exists
    const existingUser = ctx.db.user.identity.find(ctx.sender);
    if (existingUser) {
        ctx.db.user.identity.update({
            ...existingUser,
            name: userName,
            room_id: roomId,
            is_host: true
        });
    } else {
        ctx.db.user.insert({
          identity: ctx.sender,
          name: userName,
          room_id: roomId,
          is_host: true,
        });
    }
  }
);

export const joinRoom = spacetimedb.reducer(
  { roomId: t.string(), userName: t.string() },
  (ctx, { roomId, userName }) => {
    const room = ctx.db.room.id.find(roomId);
    if (!room) {
      throw new Error(`Room ${roomId} not found.`);
    }
    
    const existingUser = ctx.db.user.identity.find(ctx.sender);
    if (existingUser) {
      ctx.db.user.identity.update({
         ...existingUser,
         name: userName,
         room_id: roomId,
         is_host: room.host_identity.isEqual(ctx.sender),
      });
    } else {
      ctx.db.user.insert({
        identity: ctx.sender,
        name: userName,
        room_id: roomId,
        is_host: room.host_identity.isEqual(ctx.sender),
      });
    }
  }
);

export const updateFormData = spacetimedb.reducer(
  { roomId: t.string(), formData: t.string() },
  (ctx, { roomId, formData }) => {
    const room = ctx.db.room.id.find(roomId);
    if (!room) {
      throw new Error(`Room ${roomId} not found.`);
    }
    
    if (!room.host_identity.isEqual(ctx.sender)) {
      throw new Error(`Only the host can update the form data.`);
    }
    
    ctx.db.room.id.update({
      ...room,
      form_data: formData,
    });
  }
);

export const onDisconnect = spacetimedb.clientDisconnected(ctx => {
  const user = ctx.db.user.identity.find(ctx.sender);
  if (user) {
    ctx.db.user.identity.delete(ctx.sender);
  }
});
