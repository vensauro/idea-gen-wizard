import { schema, table, t } from 'spacetimedb/server';

const spacetimedb = schema({
  room: table(
    { public: true },
    {
      id: t.string().primaryKey(),
      team_name: t.string(),
      host_identity: t.identity(),
      form_data: t.string(),
    }
  ),
  user: table(
    { public: true },
    {
      identity: t.identity().primaryKey(),
      name: t.string(),
      room_id: t.string(),
      is_host: t.bool(),
    }
  )
});
export default spacetimedb;

